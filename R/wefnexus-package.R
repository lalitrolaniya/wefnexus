#' wefnexus: Water-Energy-Food-Nutrient-Carbon Nexus Analysis for Agronomic
#' Systems
#'
#' @description
#' Provides functions for analysing Water-Energy-Food-Nutrient-Carbon
#' (WEFNC) nexus interactions in agricultural systems. The package provides
#' five core modules and a nexus integration layer:
#'
#' \describe{
#'   \item{\strong{Water}}{Water use efficiency (WUE), water productivity (WP),
#'     water footprint (WF) with green/blue/grey components, irrigation
#'     efficiency, crop water stress index (CWSI), and crop water
#'     productivity (CWP).}
#'   \item{\strong{Energy}}{Energy budgeting including input-output analysis,
#'     energy use efficiency (EUE), energy return on investment (EROI),
#'     energy productivity (EP), net energy (NE), specific energy, and
#'     energy profitability.}
#'   \item{\strong{Food}}{Food productivity indices, harvest index (HI), land
#'     equivalent ratio (LER), system productivity index (SPI), caloric
#'     and protein yield, and production efficiency index (PEI).}
#'   \item{\strong{Nutrient}}{Nutrient use efficiency (NUE) metrics including
#'     agronomic efficiency (AE), physiological efficiency (PE), recovery
#'     efficiency (RE), partial factor productivity (PFP), internal
#'     utilization efficiency (IUE), and nutrient harvest index (NHI).}
#'   \item{\strong{Carbon}}{Carbon footprint (CF) with source-wise breakdown,
#'     greenhouse gas (GHG) emission estimation, soil organic carbon (SOC)
#'     stocks, carbon sequestration rate (CSR), and global warming potential
#'     (GWP) using IPCC AR6 values.}
#' }
#'
#' @docType package
#' @name wefnexus-package
#' @aliases wefnexus
